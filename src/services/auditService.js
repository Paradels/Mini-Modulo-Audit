const API_CONFIG = {
  minDelayMs: 300,
  maxDelayMs: 1200,
  errorRate: 0.15, // 10%-20% recomendado
};

function randomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function delay(ms) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms);
  });
}

function normalizeAudit(audit) {
  const item = { ...audit };

  if (item.status === "DONE") item.progress = 100;
  else if (item.status === "DRAFT") item.progress = 0;
  else if (item.status === "IN_PROGRESS") item.progress = Math.min(99, Math.max(1, item.progress || 1));

  return item;
}

function shouldFail(forceError, errorRate) {
  if (forceError) return true;
  return Math.random() < errorRate;
}

export function setAuditApiConfig(partial) {
  Object.assign(API_CONFIG, partial);
}

export async function fetchAudits({
  search = "",
  status = "ALL",
  page = 1,
  pageSize = 10,
  forceError = false,
  errorRate = API_CONFIG.errorRate,
} = {}) {
  await delay(randomInt(API_CONFIG.minDelayMs, API_CONFIG.maxDelayMs));

  if (shouldFail(forceError, errorRate)) {
    throw new Error("No se pudo cargar el listado. Intenta de nuevo.");
  }

  const term = search.trim().toLowerCase();

  const filtered = AUDITS.map(normalizeAudit).filter((audit) => {
    const matchStatus = status === "ALL" || audit.status === status;
    const matchSearch =
      term === "" ||
      audit.name.toLowerCase().includes(term) ||
      audit.process.toLowerCase().includes(term) ||
      audit.owner.name.toLowerCase().includes(term);

    return matchStatus && matchSearch;
  });

  const safePageSize = Math.max(1, Number(pageSize) || 10);
  const total = filtered.length;
  const metrics = {
    total,
    inProgress: filtered.filter((audit) => audit.status === "IN_PROGRESS").length,
    draft: filtered.filter((audit) => audit.status === "DRAFT").length,
    done: filtered.filter((audit) => audit.status === "DONE").length,
  };
  const totalPages = Math.max(1, Math.ceil(total / safePageSize));
  const safePage = Math.min(Math.max(1, Number(page) || 1), totalPages);

  const start = (safePage - 1) * safePageSize;
  const items = filtered.slice(start, start + safePageSize);

  return {
    items,
    total,
    metrics,
    page: safePage,
    pageSize: safePageSize,
    totalPages,
  };
}